# 练习：
#   1. 写程序，计算:
#     1 + 2 + 3 + 4 + ..... + 99 + 100的和
s = 0  # 先创建一个变量，用来存储所有数的和
i = 1  # 整数的起始数
while i <= 100:
    s += i  # 把i的值累加到s当中
    i += 1

print('和是:', s)


#   2. 写程序,任意输入一个数n,计算:
#     1 + 2 + 3 + 4 + ..... + (n-1) + n的和
n = int(input("请输入结束整数: "))

s = 0  # 先创建一个变量，用来存储所有数的和
i = 1  # 整数的起始数
while i <= n:
    s += i  # 把i的值累加到s当中
    i += 1

print('和是:', s)

