# 5. 输入一个整数n, 打印出一个长方形
#    n 代表长方形的宽度的字符数和高的行数
#    例如:
#      请输入:4
#     打印:
#       ####
#       #  #
#       #  #
#       ####
#      请输入:6
#     打印:
#       ######
#       #    #
#       #    #
#       #    #
#       #    #
#       ######

n = int(input("请输入宽/高: "))

# 第一行
print('#' * n)

# 中间行(n-2行)
i = 1
while i <= n - 2:
    # 打印每一行
    # s = '#' + ' ' * (n -2) + '#'
    # print(s)
    print('#', ' ' * (n - 2), '#', sep='')
    i += 1

# 最后一行 
print('#' * n)

