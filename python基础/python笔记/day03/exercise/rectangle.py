# 练习:
#   写一个程序，打印一个高度为4行的矩形方框
#     要求输入一个整形数字，此整数代表矩形的宽度，输出此矩形
#   如:
#     请输入矩形宽度: 10
#     ##########
#     #        #
#     #        #
#     ##########
#     请输入矩形宽度: 5
#     #####
#     #   #
#     #   #
#     #####

w = int(input('请输入矩形宽度: '))
print('#' * w)
# 打印第二行
s = '#' + ' ' * (w - 2) + '#'
print(s)
print(s)
print('#' * w)
