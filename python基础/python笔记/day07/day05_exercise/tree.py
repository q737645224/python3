# 2. 输入一个整数（代表树干的高度)
#    打印出如下一棵树
#    输入: 2
#    打印:
#     *
#    ***
#     *
#     *
#    输入:3
#      *
#     ***
#    *****
#      *
#      *
#      *

n = int(input("请输入一个树干的高度: "))

# 打印树叶部分
for i in range(1, n + 1):  # i 代表层数
    stars = i * 2 - 1  # 算出星号的个数
    blank = n - i  # 算出左侧空格的个数
    print(' ' * blank + '*' * stars)

# 打印树干部分
for _ in range(n):
    print(' ' * (n - 1) + '*')
