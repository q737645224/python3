# 3. 温度转换:
#   摄氏温度 = 5.0/9.0*(华氏温度-32)

#   请问100华氏温度是多少摄氏温度.写程序算出来
#   思考：
#     如果知道当前温度是摄氏温度100度，能否算出华氏温度
#     是多少？

f = 100
c = 5.0 / 9.0 * (f - 32)
print("摄氏温度是:", c)
# 摄氏温度 = 5.0/9.0*(华氏温度-32)
# 摄氏温度*9.0/5.0 = (华氏温度-32)
# 摄氏温度*9.0/5.0 + 32 = 华氏温度
f = 100 * 9.0/ 5.0 + 32
print("华式温度是:", f)




