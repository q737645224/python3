# 1. 输入两个整数， 分别用变量x, y绑定
#   1) 计算这两个数的和,并打印结果
#   2) 计算这两个数的积,并打印结果
#   3) 计算x的y次方并打印结果
#   如:
#     请输入x: 10
#     请输入y: 20
#   打印如下:
#     10 + 20 = 30
#     10 * 20 = 200
#     10 ** 20 = ...


# 1. 输入两个整数， 分别用变量x, y绑定
x = input("请输入x: ")
x = int(x)
y = input("请输入y: ")
y = int(y)

# 1) 计算这两个数的和,并打印结果
print(x, '+', y, '=', x + y)
# 2) 计算这两个数的积,并打印结果
print(x, '*', y, '=', x * y)
# 3) 计算x的y次方并打印结果
print(x, '**', y, '=', x ** y)

