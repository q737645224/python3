# 3 写函数f(n)求下多项式的和
# fn = 1/1 - 1/3 + 1/5 - 1/7 + 1/9 .... 1/(2*n-1)的和

# 求当n取值为1000000时，
#   1） 打印 f(1000000) 的值
#   2） 打印 f(1000000) * 4的值，看看是什么

def f(n):
    s = 0
    sign = 1  # 符号(正负号)
    for i in range(1, n + 1):
        s += sign * 1 / (2 * i - 1)
        sign *= -1

    return s

# 1） 打印 f(1000000) 的值
print(f(1000000))
# 2） 打印 f(1000000) * 4的值，看看是什么
print(f(1000000) * 4)

